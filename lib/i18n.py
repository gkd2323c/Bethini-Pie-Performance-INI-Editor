TOKEN_RE = re.compile(r"\d+[A-Za-z]+|[A-Za-z][A-Za-z0-9\-\+']*|\s+|[^\sA-Za-z]+")
PLACEHOLDER_LABEL_RE = re.compile(r"^(?:emptylabel\d*|nolabelframe\d*)$", re.IGNORECASE)
CAMEL_CASE_RE = re.compile(r"[A-Z]+(?=[A-Z][a-z]|\d|$)|[A-Z]?[a-z]+|\d+")


def _restore_whitespace(original: str, translated: str) -> str:
    leading = len(original) - len(original.lstrip())
    trailing = len(original) - len(original.rstrip())
    return f"{original[:leading]}{translated}{original[len(original)-trailing:] if trailing else ''}"


def _translate_token(token: str) -> str:
    if token.isspace():
        return token
    if token in EXACT_TRANSLATIONS:
        return EXACT_TRANSLATIONS[token]
    lower = token.lower()
    if lower in WORD_TRANSLATIONS:
        return WORD_TRANSLATIONS[lower]

    # Translate hyphenated terms part-by-part (for example: Sun-Shadow).
    if "-" in token and token.count("-") <= 3:
        parts = token.split("-")
        translated_parts = [_translate_token(part) for part in parts]
        if translated_parts != parts:
            return "-".join(translated_parts)

    # Translate simple camelCase/PascalCase tokens (for example: fixedDefault).
    if re.search(r"[a-z][A-Z]", token):
        parts = CAMEL_CASE_RE.findall(token)
        if len(parts) > 1:
            translated_parts = [_translate_token(part) for part in parts]
            if translated_parts != parts:
                return " ".join(translated_parts)
    return token


def translate_ui_text(text: str | None) -> str:
    """Translate visible UI text to Simplified Chinese when possible."""
    if text is None:
        return ""
    if text == "":
        return ""

    stripped = text.strip()
    if not stripped:
        return text

    if PLACEHOLDER_LABEL_RE.match(stripped):
        return _restore_whitespace(text, "")

    if stripped.startswith("Save on "):
        save_target = translate_ui_text(stripped[len("Save on "):]).strip()
        return _restore_whitespace(text, f"{save_target}时保存")

    direct = EXACT_TRANSLATIONS.get(stripped)
    if direct is not None:
        return _restore_whitespace(text, direct)

    # Keep technical paths/identifiers untouched.
    if stripped.startswith("http"):
        return text
    if ("\\" in stripped or "/" in stripped) and " " not in stripped and "\n" not in stripped:
        return text

    if "\n" in text:
        return "\n".join(translate_ui_text(line) for line in text.split("\n"))

    translated = stripped
    for src, dst in PHRASE_TRANSLATIONS:
        translated = translated.replace(src, dst)

    tokens = TOKEN_RE.findall(translated)
    out = "".join(_translate_token(token) for token in tokens)
    out = out.replace("  ", " ")
    return _restore_whitespace(text, out)
